public struct CharacterAttributes
{
    public int Level;           // character level
    public int Might;           // damage
    public int Dexterity;       // crit rating, crit damage, movement spaces
    public int Intellect;       // mana, mana regen, resistances
    public int Vitality;        // health, health regen
    public int Reflex;          // counter, dodge
}